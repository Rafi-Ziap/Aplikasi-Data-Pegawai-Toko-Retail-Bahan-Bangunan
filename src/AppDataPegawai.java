    private void buttonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHapusActionPerformed
        String id = inputID.getText();
        Pegawai target = null;

        for (Pegawai p : daftarPegawai) {
            if (p.getId().equals(id)) {
                target = p;
                break;
            }
        }

        if (target != null) {
            daftarPegawai.remove(target);
            javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Data dengan ID tersebut tidak ditemukan.");
        }

        tampilkanData();
    }//GEN-LAST:event_buttonHapusActionPerformed

    private void buttonUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUbahActionPerformed
        String id = inputID.getText();

        for (Pegawai p : daftarPegawai) {
            if (p.getId().equals(id)) {
                // Update data
                String nama = inputNama.getText();
                String usia = inputUsia.getText();
                String alamat = inputAlamat.getText();
                String divisi = comboBoxDivisi.getSelectedItem().toString();
                String shift = shiftPagi.isSelected() ? "Pagi" : "Malam";

                p.setAll(nama, usia, alamat, divisi, shift);
                javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil diubah.");
                tampilkanData();
                return;
            }
        }

        javax.swing.JOptionPane.showMessageDialog(this, "Data dengan ID tersebut tidak ditemukan.");
    }//GEN-LAST:event_buttonUbahActionPerformed

    private void tampilkanData(){
        dataPegawai.setText(""); // Bersihkan terlebih dahulu
        for (Pegawai p : daftarPegawai) {
            dataPegawai.append(p.toString() + "\n");
        }
    }
