    private void buttonTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTambahActionPerformed
        String nama = inputNama.getText();
        String id = inputID.getText();
        String usia = inputUsia.getText();
        String alamat = inputAlamat.getText();
        String divisi = comboBoxDivisi.getSelectedItem().toString();
        String shift = shiftPagi.isSelected() ? "Pagi" : "Malam"; 
        //Ternary operator: bentuk singkat dari if-else statement sederhana

        // Cek duplikat ID
        for (Pegawai p : daftarPegawai) {
            if (p.getId().equals(id)) {
                javax.swing.JOptionPane.showMessageDialog(this, "ID sudah digunakan!");
                return;
            }
        }

        Pegawai pegawai = new Pegawai(nama, id, usia, alamat, divisi, shift);
        daftarPegawai.add(pegawai);

        tampilkanData();
    }//GEN-LAST:event_buttonTambahActionPerformed

